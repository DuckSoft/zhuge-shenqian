function getStrokes(char) {
    for (var lns in bh) {
        var arr = bh[lns].split("");
        for (var ch in arr) {
            if (char === arr[ch]) {
                return lns;
            }
        }
    }

    return 0;
}

function getNumber(words) {
    var number = 0;
    words.split("").reverse().forEach(function (value) {
        number *= 10;
        number += (getStrokes(value))%10;
    });
    number %= 384;
    return number;
}

